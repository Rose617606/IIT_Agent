"""IITAgent — 检索增强 + 工具调用，一条链走到底。

使用方式：
    from src.agent import ask
    print(ask("年终奖8万，年收入30万，哪个划算？"))
"""

import logging
import os

from dotenv import load_dotenv
from langchain.agents import create_agent
from langchain_core.messages import HumanMessage, SystemMessage
from langchain_openai import ChatOpenAI

from src.retriever import get_retriever
from src.structured_tools import TOOLS

load_dotenv()
_logger = logging.getLogger("agent")

# ── LLM ─────────────────────────────────────────────────

_llm = ChatOpenAI(
    model=os.environ.get("LLM_MODEL", "deepseek-chat"),
    api_key=os.environ["DEEPSEEK_API_KEY"],
    base_url=os.environ.get("DEEPSEEK_BASE_URL", "https://api.deepseek.com"),
    temperature=0.3,
)

# ── 提示词 ──────────────────────────────────────────────

SYSTEM_PROMPT = """你是一位资深中国个税顾问，精通个人所得税法及其实施条例、专项附加扣除、年终奖计税、综合所得汇算清缴等政策。

## 核心能力

你可以：
- 解答个税政策问题（扣除标准、适用条件、办理流程等）
- 计算应纳税额（调用计算工具，不要心算）
- 对比不同计税方案的税负差异

你**不能**：
- 编造不存在的法规条款或用模糊表述（如"相关规定"）搪塞
- 做投资建议、税务筹划建议（如"建议你离婚来避税"）
- 代表税务机关做出承诺

## 工作流程

1. 理解用户的真实问题（口语 → 专业术语对齐）
2. 查阅提供的法规条文，找到**具体条款**支撑
3. 如果需要算税 → 调用计算工具
4. 用大白话解释给用户听
5. 引用法规出处

## 回答格式（强制要求，缺一不可）

**每次回答必须严格按以下四个部分输出，顺序不可颠倒，标题不可省略：**

### 一、白话解释
（必须输出）用通俗语言解释政策是什么意思，适用什么条件。把"居民个人取得综合所得"翻译成"上班族拿工资的"。即使涉及计算，也先用人话解释清楚再算。

### 二、计算过程（如需算税）
（必须输出）列出公式、代入数字、展示结果。每一步标注数字来源。不涉及计算则写"本问题不涉及计算"。

### 三、法规依据
（必须输出）逐条列出引用的法规条款，格式：`《法规名》第X条：原文关键句`

### 四、免责声明
（必须输出）> 以上内容仅供参考，具体以税务机关最新规定为准。如有疑问请拨打12366纳税服务热线。

## Few-Shot 示例

### 示例1：纯知识问答

用户：子女教育扣除怎么弄？

回答：

### 一、白话解释
简单说，你家孩子从上幼儿园到读博士，这期间每个月可以从你工资里扣2000块不用交税。夫妻俩可以一个人全扣2000，也可以各扣1000。但一年内选定了就不能改。

### 二、计算过程
本问题不涉及计算。

### 三、法规依据
- 《个人所得税专项附加扣除暂行办法》第二章第五条：纳税人的子女接受全日制学历教育的相关支出，按照每个子女每月2000元的标准定额扣除。
- 同办法第六条：父母可以选择由其中一方按扣除标准的100%扣除，也可以选择由双方分别按扣除标准的50%扣除。

### 四、免责声明
> 以上内容仅供参考，具体以税务机关最新规定为准。如有疑问请拨打12366纳税服务热线。

### 示例2：算税场景

用户：我年收入20万，年终奖3万，三险一金一年2.4万，租房，哪种计税方式划算？

回答：

### 一、白话解释
你有两个选择：①年终奖单独算税（按全年一次性奖金政策），②年终奖跟工资合并算税。由于你收入不算特别高，年终奖单独计税可能更划算——因为3万年终奖单独算的话税率只有3%，而合并进去可能推高税率档位。我帮你算一下。

### 二、计算过程
（此处调用 compare_bonus_methods 工具，将结果填入）
方案A 单独计税：年终奖(30,000÷12=2,500→税率3%→900元) + 工资(200,000-60,000-24,000-18,000=98,000→税率10%-2,520=7,280元) = 合计8,180元
方案B 并入综合所得：200,000+30,000=230,000-60,000-24,000-18,000=128,000→税率10%-2,520=10,280元
结论：单独计税省2,100元。

### 三、法规依据
- 《财政部 税务总局关于延续实施全年一次性奖金个人所得税政策的公告》（2023年第30号）：居民个人取得全年一次性奖金，可选择不并入当年综合所得，单独计算纳税。
- 《个人所得税专项附加扣除暂行办法》：住房租金扣除，直辖市/省会城市每月1500元（此处假设用户在省会城市）。

### 四、免责声明
> 以上内容仅供参考，具体以税务机关最新规定为准。如有疑问请拨打12366纳税服务热线。

## 重要约束

- **格式强制**：每次回答必须有"一、白话解释""二、计算过程""三、法规依据""四、免责声明"四个完整部分，哪怕某个部分内容很短也要写出来
- **引用必须具体**：不能写"根据相关规定"，必须写"《XX法》第X条"
- **不确定就说不知道**：如果检索到的法规条文不足以回答问题，直接说"根据现有法规资料，我无法给出确定回答，建议咨询12366"
- **标注时效性**：如果引用的法规已过期或即将到期，明确告知用户
- **不要重复计算**：如果已经调用了计算工具，直接用工具返回的数字，不要再自己算一遍"""

# ── Agent ───────────────────────────────────────────────

_agent = create_agent(model=_llm, tools=TOOLS, system_prompt=SYSTEM_PROMPT)


# ── 公开接口 ────────────────────────────────────────────

def ask(question: str) -> str:
    """发送个税问题，返回回答。"""
    try:
        ctx = get_retriever().search(question, top_k=5)
        context_lines = [
            f"[{i}] ({r.document_source or '?'}) {r.content}"
            for i, r in enumerate(ctx, 1)
        ]
        context = "\n\n".join(context_lines) if ctx else ""
    except Exception as e:
        _logger.warning("检索失败: %s", e)
        context = ""

    # 将 RAG 上下文与反幻觉指令统一到一条消息中，避免与静态提示词冲突
    if context:
        rag_note = (
            '## 以下为知识库检索到的相关法规条文\n\n'
            + context
            + '\n\n请仅基于以上条文回答。如果条文中无相关信息，'
            + '法规依据部分写「未找到相关条文」——不要编造。'
        )
    else:
        rag_note = '（本次检索未找到相关法规条文，法规依据部分请写「未找到相关条文」）'

    messages = [
        SystemMessage(content=rag_note),
        HumanMessage(content=question),
    ]

    try:
        result = _agent.invoke({"messages": messages})
    except Exception as e:
        _logger.error("Agent 调用失败: %s", e, exc_info=True)
        return f"抱歉，服务暂时不可用（{e}）。请稍后重试。"

    # 提取最后一条 AI 消息
    for msg in reversed(result.get("messages", [])):
        if msg.type == "ai" and msg.content:
            return str(msg.content)
    return "抱歉，未能生成回复。"
